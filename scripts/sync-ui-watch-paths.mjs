/**
 * Regenerate scripts/ui-watch-paths.inc.sh from src/lib.rs hydrate module graph.
 * Run after adding/removing WASM-facing modules.
 *
 * Usage:
 *   node scripts/sync-ui-watch-paths.mjs
 *   node scripts/sync-ui-watch-paths.mjs --check
 */
import { existsSync, readFileSync, writeFileSync } from "node:fs";
import path from "node:path";

const root = process.cwd();
const libRs = path.join(root, "src/lib.rs");
const out = path.join(root, "scripts/ui-watch-paths.inc.sh");
const checkOnly = process.argv.includes("--check");

function parseModNames(text) {
  const mods = [];
  let skipNextMod = false;

  for (const line of text.split("\n")) {
    const trimmed = line.trim();
    if (/^#\[cfg\(feature = "ssr"\)\]/.test(trimmed)) {
      skipNextMod = true;
      continue;
    }
    const m = trimmed.match(/^pub mod ([a-z_][a-z0-9_]*);$/);
    if (m) {
      if (!skipNextMod) {
        mods.push(m[1]);
      }
      skipNextMod = false;
      continue;
    }
    if (trimmed && !trimmed.startsWith("//")) {
      skipNextMod = false;
    }
  }

  return mods;
}

function addPath(paths, rel) {
  paths.add(rel);
}

function resolveModulePaths(paths, modName) {
  const file = `src/${modName}.rs`;
  const dir = `src/${modName}`;
  const modRs = path.join(root, dir, "mod.rs");

  if (existsSync(path.join(root, file))) {
    addPath(paths, file);
    return;
  }

  if (!existsSync(path.join(root, dir))) {
    return;
  }

  if (existsSync(modRs)) {
    for (const sub of parseModNames(readFileSync(modRs, "utf8"))) {
      addPath(paths, `${dir}/${sub}.rs`);
    }
    return;
  }

  addPath(paths, dir);
}

const paths = new Set(["src/lib.rs"]);
const topMods = parseModNames(readFileSync(libRs, "utf8"));

for (const name of topMods) {
  if (name === "crawler") {
    continue;
  }
  resolveModulePaths(paths, name);
}

const sorted = [...paths].sort();
const regexParts = sorted.map((p) => {
  if (p.endsWith(".rs")) {
    return p.replace(/\./g, "\\.");
  }
  return `${p.replace(/\./g, "\\.")}/.*\\.rs`;
});

const body = `#!/usr/bin/env bash
# AUTO-GENERATED by scripts/sync-ui-watch-paths.mjs — do not edit by hand.
# Source: src/lib.rs hydrate module graph (excludes #[cfg(ssr)] modules).

ui_watch_paths=(
${sorted.map((p) => `  ${p}`).join("\n")}
)

ui_path_re='^(${regexParts.join("|")})$'
`;

if (checkOnly) {
  if (!existsSync(out)) {
    console.error(`Missing ${out}; run: node scripts/sync-ui-watch-paths.mjs`);
    process.exit(1);
  }
  const current = readFileSync(out, "utf8");
  if (current !== body) {
    console.error(`${out} is out of sync with src/lib.rs; run: node scripts/sync-ui-watch-paths.mjs`);
    process.exit(1);
  }
  console.log(`OK: ${out} in sync (${sorted.length} watch paths)`);
  process.exit(0);
}

writeFileSync(out, body, { mode: 0o755 });
console.log(`Wrote ${out} (${sorted.length} watch paths)`);