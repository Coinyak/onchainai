/**
 * Regenerate scripts/ui-watch-paths.inc.sh from the Next.js frontend tree.
 * Run after adding/removing UI-facing frontend modules.
 *
 * Usage:
 *   node scripts/sync-ui-watch-paths.mjs
 *   node scripts/sync-ui-watch-paths.mjs --check
 */
import { existsSync, readdirSync, readFileSync, statSync, writeFileSync } from "node:fs";
import path from "node:path";

const root = process.cwd();
const out = path.join(root, "scripts/ui-watch-paths.inc.sh");
const checkOnly = process.argv.includes("--check");

const FRONTEND_DIRS = ["frontend/app", "frontend/components", "frontend/lib"];
const FRONTEND_FILES = [
  "frontend/next.config.ts",
  "frontend/tailwind.config.ts",
  "frontend/postcss.config.mjs",
  "frontend/app/globals.css",
];

const UI_EXTENSIONS = new Set([".ts", ".tsx", ".css", ".mjs"]);

function walkDir(relDir, paths) {
  const abs = path.join(root, relDir);
  if (!existsSync(abs)) {
    return;
  }
  for (const entry of readdirSync(abs)) {
    const rel = path.join(relDir, entry);
    const absEntry = path.join(root, rel);
    const st = statSync(absEntry);
    if (st.isDirectory()) {
      if (entry === "node_modules" || entry === ".next") {
        continue;
      }
      walkDir(rel, paths);
      continue;
    }
    if (UI_EXTENSIONS.has(path.extname(entry))) {
      paths.add(rel.split(path.sep).join("/"));
    }
  }
}

const paths = new Set();

for (const dir of FRONTEND_DIRS) {
  if (existsSync(path.join(root, dir))) {
    paths.add(dir);
    walkDir(dir, paths);
  }
}

for (const file of FRONTEND_FILES) {
  if (existsSync(path.join(root, file))) {
    paths.add(file);
  }
}

const sorted = [...paths].sort();
const regexParts = sorted.map((p) => {
  if (p.endsWith(".ts") || p.endsWith(".tsx") || p.endsWith(".css") || p.endsWith(".mjs")) {
    return p.replace(/\./g, "\\.");
  }
  return `${p.replace(/\./g, "\\.")}/.*\\.(ts|tsx|css|mjs)`;
});

const body = `#!/usr/bin/env bash
# AUTO-GENERATED by scripts/sync-ui-watch-paths.mjs — do not edit by hand.
# Source: Next.js frontend (frontend/app, components, lib, config).

ui_watch_paths=(
${sorted.map((p) => `  ${p}`).join("\n")}
)

ui_path_re='^(${regexParts.join("|")})$'
`;

if (checkOnly) {
  if (!existsSync(out)) {
    console.error(`Missing ${out}; run: node scripts/sync-ui-watch-paths.mjs`);
    process.exit(1);
  }
  const current = readFileSync(out, "utf8");
  if (current !== body) {
    console.error(`${out} is out of sync with frontend/; run: node scripts/sync-ui-watch-paths.mjs`);
    process.exit(1);
  }
  console.log(`OK: ${out} in sync (${sorted.length} watch paths)`);
  process.exit(0);
}

writeFileSync(out, body, { mode: 0o755 });
console.log(`Wrote ${out} (${sorted.length} watch paths)`);